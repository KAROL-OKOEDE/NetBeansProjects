    public void init(String username) {
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/chatapp", "root", "KAROL2005");
            PreparedStatement ps = con.prepareStatement("SELECT * FROM login WHERE Username = ?");
            ps.setString(1, username);

            ResultSet rs = ps.executeQuery();

            if (rs.next()) {
                jTextField1.setText(rs.getString(1));
                jTextField2.setText(rs.getString(2));
                jTextField3.setText(rs.getString(3));
                jTextField4.setText(rs.getString(4));
                jTextField5.setText(rs.getString(5));
                jPasswordField1.setText(rs.getString(6));

                byte[] images = rs.getBytes(7);
                if (images != null) {
                    ImageIcon originalIcon = new ImageIcon(images);
                    Image scaledImage = originalIcon.getImage().getScaledInstance(jLabel30.getWidth(), jLabel30.getHeight(), Image.SCALE_SMOOTH);
                    ImageIcon scaledIcon = new ImageIcon(scaledImage);
                    jLabel30.setIcon(scaledIcon);
                } else {
                    jLabel30.setIcon(null);
                }
            } else {
                JOptionPane.showMessageDialog(rootPane, "No Username was found with this: " + username);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
    }

