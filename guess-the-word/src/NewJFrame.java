   public static String scramble(String text){
       char[] chars = text.toCharArray();
       Random random = new Random();
       for (int i = 0; i < chars.length; i++){
           int j = random.nextInt(chars.length);
           char temp = chars[i];
           chars[i] =  chars[j];
           chars[j] = temp;
       }
       return new String(chars);
   };
   

   
   


